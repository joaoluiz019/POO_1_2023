package disciplinas;

public class Professores {
	
	String nome;
	int formacao;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getFormacao() {
		return formacao;
	}
	public void setFormacao(int formacao) {
		this.formacao = formacao;
	}

}
